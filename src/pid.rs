
//! # Высокоточный промышленный ПИД-регулятор с безударным переходом и фильтрацией шумов.
//!
//! ## Математическая модель
//!
//! ### Непрерывная форма (идеальный ПИД):
//! ```text
//! u(t) = Kp * e(t) + Ki * ∫e(t)dt + Kd * de(t)/dt
//! ```
//! где:
//! - `u(t)` - выходной сигнал управления
//! - `e(t) = SP(t) - PV(t)` - ошибка рассогласования
//! - `Kp` - пропорциональный коэффициент
//! - `Ki = 1/Ti` - интегральный коэффициент (Ti - время интегрирования)
//! - `Kd = Td` - дифференциальный коэффициент (Td - время упреждения)
//!
//! ### Дискретная форма (Tustin approximation / Trapezoidal rule):
//! ```text
//! P_k = Kp * e_k
//! I_k = I_{k-1} + Ki * Ts * (e_k + e_{k-1}) / 2
//! D_k = Kd * (PV_k - PV_{k-1}) / Ts  (filtered)
//! u_k = P_k + I_k + D_k
//! ```
//!
//! ### Формулы компонентов:
//!
//! **1. Ошибка регулирования:**
//! ```text
//! e_k = SP_k - PV_k
//! ```
//!
//! **2. Зона нечувствительности (Deadband):**
//! ```text
//! e_effective = {
//!     e_k - db_max,  if e_k > db_max     (линейный сдвиг)
//!     e_k - db_min,  if e_k < db_min     (линейный сдвиг)
//!     0,             if db_min ≤ e_k ≤ db_max
//! }
//! ```
//!
//! **3. Пропорциональная составляющая:**
//! ```text
//! P_k = Kp * e_effective
//! ```
//!
//! **4. Дифференциальная составляющая с фильтром:**
//! ```text
//! raw_derivative = (PV_k - PV_{k-1}) / Ts                    [°C/s]
//! filtered = alpha * D_{k-1} + (1 - alpha) * (-Kd * raw_derivative)
//! D_k = filtered
//! ```
//! где коэффициент фильтра:
//! ```text
//! alpha = 1 / (1 + Kdf * Ts)
//! Kdf - частота среза фильтра [рад/с]
//! ```
//!
//! **5. Интегральная составляющая (Trapezoidal rule):**
//! ```text
//! I_k = I_{k-1} + Ki * Ts * (e_k + e_{k-1}) / 2
//! I_k = clamp(I_k, op_min, op_max)
//! ```
//!
//! **6. Результирующий сигнал:**
//! ```text
//! u_raw = P_k + I_k + D_k
//! u_k = clamp(u_raw, op_min, op_max)
//! ```
//!
//! ### Анти-виндап защита (Clamping method):
//! ```text
//! Integrator active IF:
//!   u_raw ≤ op_max OR (u_raw > op_max AND e_k ≤ 0)  // не в верхнем насыщении
//!   AND
//!   u_raw ≥ op_min OR (u_raw < op_min AND e_k ≥ 0)  // не в нижнем насыщении
//! ```
//!
//! ### Безударное переключение (Bumpless Transfer):
//!
//! **Ручной → Автомат:**
//! ```text
//! I_k = u_manual - P_k - D_k
//! I_k = clamp(I_k, op_min, op_max)
//! ```
//!
//! **Автомат → Ручной:**
//! ```text
//! manual_val = u_k  (постоянная синхронизация)
//! ```
//!
//! ## Характеристики системы
//! - **Тип регулятора:** ПИД с фильтрацией производной (PIDF)
//! - **Метод дискретизации:** Tustin (трапецеидальный) для интегратора, Backward Euler для фильтра
//! - **Защита от интегрального насыщения:** Conditional Integration (Clamping)
//! - **Безударный переход:** Двунаправленный (Auto↔Manual)
//! - **Фильтрация:** ФНЧ 1-го порядка для D-составляющей
//! - **Нелинейность:** Линейно-сдвинутая зона нечувствительности
//!
//! # Инструкция по использованию
//!
//! ## 1. Создание регулятора
//! ```text
//! let mut pid = PidController::new(ts);
//! ```
//! где `ts` - период вызова `tick()` в секундах (например, 0.1 для 100 мс)
//!
//! ## 2. Настройка параметров
//! ```text
//! pid.set_parameters(
//!     kp,     // Пропорциональный коэффициент. Типично: 0.5-5.0
//!     ki,     // Интегральный коэфф. (1/Ti). Типично: 0.01-0.5 (Ti=2-100с)
//!     kd,     // Диффер. коэфф. (Td). Типично: 0.1-5.0 (сек)
//!     kdf,    // Частота среза фильтра [рад/с]. Типично: 5-20
//!     db_min, // Нижняя граница deadband (обычно -0.5..0)
//!     db_max, // Верхняя граница deadband (обычно 0..0.5)
//!     op_min, // Мин. выход (обычно 0% или -100%)
//!     op_max, // Макс. выход (обычно 100%)
//! );
//! ```
//!
//! ## 3. Циклический вызов
//! ```text
//! loop {
//!     pid.update_inputs(setpoint, process_value, manual_val, manual_mode);
//!     pid.tick();
//!     let output = pid.get_output();
//!     apply_output(output);
//!     sleep(ts);
//! }
//! ```
//!
//! ## 4. Пример настройки для горелки 2 МВт
//! ```text
//! // Параметры котла:
//! // - Мощность: 2 МВт
//! // - Объем воды: 500 м³
//! // - Расход: 25 м³/ч
//! // - Диапазон температур: 60-95°C
//! // - Постоянная времени нагрева: ~5-8 часов
//! 
//! let mut pid = PidController::new(0.1); // 100 мс шаг
//! 
//! // Консервативная настройка для медленного теплового процесса
//! pid.set_parameters(
//!     2.0,    // Kp: 1°C ошибки → 2% мощности (40 кВт)
//!     0.02,   // Ki: Ti=50с (медленное устранение статической ошибки)
//!     1.5,    // Kd: Td=1.5с (демпфирование колебаний)
//!     5.0,    // Kdf: частота среза фильтра 5 рад/с
//!     -0.3,   // db_min: -0.3°C deadband
//!     0.3,    // db_max: +0.3°C deadband
//!     0.0,    // op_min: модуляция горелки от 0%
//!     100.0,  // op_max: до 100% мощности
//! );
//! 
//! // Управление по температуре 80°C
//! pid.update_inputs(80.0, current_temp, 0.0, false);
//! pid.tick();
//! let power_percent = pid.get_output();
//! let power_kw = power_percent * 20.0; // 2000 кВт / 100%
//! ```
//!
//! ## 5. Рекомендации по настройке
//! 
//! **Для медленных процессов (нагрев/охлаждение):**
//! - Начинайте с Kp = 1-3, Ki = 0.01-0.05, Kd = 0.5-2.0
//! - Увеличивайте Ki если долго выходит на уставку
//! - Увеличивайте Kd если есть перерегулирование
//!
//! **Для быстрых процессов (расход, давление):**
//! - Начинайте с Kp = 0.5-2.0, Ki = 0.1-0.5, Kd = 0.1-0.5
//! - Уменьшайте шаг дискретизации для быстрой динамики
//!
//! **Выбор Kdf:**
//! ```text
//! f_cutoff = Kdf / (2π)  // Частота среза в Гц
//! f_cutoff должно быть в 5-10 раз меньше частоты Найквиста (1/(2*Ts))
//! Типичные значения Kdf для Ts=0.1с: 5-20 рад/с (0.8-3.2 Гц)
//! ```
//!
//! **Выбор Deadband:**
//! - Для температуры: ±0.2..±0.5°C
//! - Для давления: ±0.1..±0.5 bar
//! - Для расхода: ±0.5..±2% от диапазона
//!
//! ## 6. Диагностика и отладка
//! 
//! Проверка корректности работы:
//! ```text
//! 1. Подайте ступенчатое изменение уставки на 10%
//! 2. Убедитесь что:
//!    - Выход реагирует без задержки (P-составляющая)
//!    - Выход плавно растет/падает до насыщения (I-составляющая)
//!    - Выход возвращается при приближении к уставке (D-составляющая)
//!    - При переключении Manual↔Auto нет скачков выхода
//!    - В deadband выход стабилен (нет микро-осцилляций)
//! ```

/// Структура ПИД-регулятора, инкапсулирующая параметры и внутреннее состояние.
#[derive(Debug, Clone)]
pub struct PidController {
    // --- Настройки (Конфигурация) ---
    kp: f64,      // Kp: Proportional gain
    ki: f64,      // Ki = 1/Ti [1/s]: Integral coefficient (Ti - интегральное время)
    kd: f64,      // Kd = Td [s]: Derivative coefficient (Td - время упреждения)
    kdf: f64,     // Kdf [rad/s]: Частота среза фильтра НЧ для D-составляющей (typical: 5-20)
    db_min: f64,  // Deadband minimum: нижняя граница зоны нечувствительности (typical: -0.5..0)
    db_max: f64,  // Deadband maximum: верхняя граница зоны нечувствительности (typical: 0..0.5)
    op_min: f64,  // Output minimum: минимальное значение выхода (typical: 0.0)
    op_max: f64,  // Output maximum: максимальное значение выхода (typical: 100.0)
    ts: f64,      // Ts [s]: Период дискретизации (sampling time)

    // --- Переменные состояния (State variables) ---
    i_part: f64,      // I_k: Аккумулированная интегральная составляющая
    d_state: f64,     // D_{k-1}: Предыдущее состояние фильтра производной
    prev_er: f64,     // e_{k-1}: Ошибка на предыдущем шаге (для метода трапеций)
    prev_pv: f64,     // PV_{k-1}: Предыдущее значение процесса (для расчета производной)
    first_run: bool,  // Флаг инициализации (предотвращает diff-kick при старте)

    // --- Входы и выходы (I/O) ---
    pv: f64,      // PV: Process Variable - текущее значение регулируемой величины
    sp: f64,      // SP: Set Point - уставка (желаемое значение)
    manual: f64,  // Manual: значение ручного управления (используется при man_on=true)
    man_on: bool, // Manual mode: true = ручной режим, false = автоматический
    op: f64,      // OP: Output - выходной сигнал управления (clamped to [op_min, op_max])
}

impl PidController {
    /// Создает новый экземпляр ПИД-регулятора со значениями по умолчанию.
    /// 
    /// # Аргументы
    /// * `ts` - период дискретизации [секунды]. Должен быть > 0.
    ///   При ts ≤ 0 автоматически устанавливается ts = 0.001 с (1 кГц).
    /// 
    /// # Пример
    /// ```
    /// let pid = PidController::new(0.1); // 100 мс период
    /// ```
    pub fn new(ts: f64) -> Self {
        let safe_ts = if ts <= 0.0 { 0.001 } else { ts };
        Self {
            kp: 0.0,
            ki: 0.0,
            kd: 0.0,
            kdf: 10.0, // Частота среза фильтра по умолчанию 10 рад/с
            db_min: 0.0,
            db_max: 0.0,
            op_min: 0.0,
            op_max: 100.0,
            ts: safe_ts,
            i_part: 0.0,
            d_state: 0.0,
            prev_er: 0.0,
            prev_pv: 0.0,
            first_run: true,
            pv: 0.0,
            sp: 0.0,
            manual: 0.0,
            man_on: false,
            op: 0.0,
        }
    }

    /// Установка физических параметров настройки регулятора.
    /// 
    /// Все коэффициенты (kp, ki, kd) передаются в стандартном физическом (непрерывном) представлении.
    /// Масштабирование на шаг дискретизации `ts` выполняется автоматически внутри регулятора.
    /// 
    /// # Аргументы
    /// * `kp` - пропорциональный коэффициент (gain), безразмерный
    /// * `ki` - интегральный коэффициент [1/с], равен 1/Ti где Ti - время интегрирования
    /// * `kd` - дифференциальный коэффициент [с], равен Td - времени упреждения
    /// * `kdf` - частота среза фильтра D-составляющей [рад/с]
    /// * `db_min`, `db_max` - границы зоны нечувствительности (в единицах PV)
    /// * `op_min`, `op_max` - ограничения выходного сигнала (обычно %)
    /// 
    /// # Примеры
    /// ```
    /// // Настройка для медленного теплового процесса (котел 500м³)
    /// pid.set_parameters(2.0, 0.02, 1.5, 5.0, -0.3, 0.3, 0.0, 100.0);
    /// 
    /// // Настройка для быстрого контура расхода
    /// pid.set_parameters(0.8, 0.2, 0.3, 15.0, -0.1, 0.1, 0.0, 100.0);
    /// ```
    pub fn set_parameters(
        &mut self,
        kp: f64,
        ki: f64,
        kd: f64,
        kdf: f64,
        db_min: f64,
        db_max: f64,
        op_min: f64,
        op_max: f64,
    ) {
        self.kp = kp;
        // Ki и Kd принудительно неотрицательные для устойчивости
        self.ki = ki.max(0.0);
        self.kd = kd.max(0.0);
        
        // Ограничиваем частоту среза фильтра частотой Найквиста для обеспечения устойчивости
        // f_nyquist = 1/(2*Ts), ω_nyquist = 2π*f_nyquist = π/Ts
        let nyquist_freq = std::f64::consts::PI / self.ts;
        if kdf <= 0.0 {
            self.kdf = 1.0; // Минимальная частота 1 рад/с
        } else if kdf > nyquist_freq {
            // Запас устойчивости 20% ниже предела Найквиста
            self.kdf = nyquist_freq * 0.8;
        } else {
            self.kdf = kdf;
        }

        // Автоматическое исправление перепутанных границ диапазона выхода
        if op_max < op_min {
            self.op_min = op_max;
            self.op_max = op_min;
        } else {
            self.op_min = op_min;
            self.op_max = op_max;
        }

        // Автоматическое исправление перепутанных границ зоны нечувствительности
        if db_max < db_min {
            self.db_min = db_max;
            self.db_max = db_min;
        } else {
            self.db_min = db_min;
            self.db_max = db_max;
        }
    }

    /// Задание входов процесса перед вызовом tick().
    /// 
    /// # Аргументы
    /// * `sp` - Set Point: желаемое значение регулируемой величины
    /// * `pv` - Process Variable: текущее измеренное значение
    /// * `manual` - значение ручного управления (активно только при man_on=true)
    /// * `man_on` - флаг ручного режима: true = ручной, false = автоматический
    /// 
    /// # Пример
    /// ```
    /// // Автоматический режим
    /// pid.update_inputs(80.0, 75.3, 0.0, false);
    /// 
    /// // Ручной режим (оператор установил 45%)
    /// pid.update_inputs(80.0, 75.3, 45.0, true);
    /// ```
    pub fn update_inputs(&mut self, sp: f64, pv: f64, manual: f64, man_on: bool) {
        self.sp = sp;
        self.pv = pv;
        self.manual = manual;
        self.man_on = man_on;
    }

    /// Получить текущий выходной сигнал управления.
    /// 
    /// Возвращает значение в диапазоне [op_min, op_max].
    /// Вызывайте после каждого tick() для получения актуального значения.
    pub fn get_output(&self) -> f64 {
        self.op
    }

    /// Сброс внутренних интеграторов и состояний.
    /// 
    /// Необходимо вызывать при:
    /// - Перезапуске технологического процесса
    /// - Переходе из аварийного режима
    /// - Смене режима работы оборудования
    /// - Реинициализации контура управления
    pub fn reset_states(&mut self) {
        self.i_part = 0.0;
        self.d_state = 0.0;
        self.prev_er = 0.0;
        self.prev_pv = 0.0;
        self.first_run = true;
    }

    /// Основной шаг расчета ПИД-алгоритма.
    /// 
    /// Должен вызываться строго с периодом `ts`, заданным при создании.
    /// 
    /// # Алгоритм (последовательность вычислений):
    /// 1. Инициализация при первом вызове (prev_pv = pv)
    /// 2. Расчет ошибки: e = SP - PV
    /// 3. Применение зоны нечувствительности к ошибке
    /// 4. Расчет P-составляющей: P = Kp * e
    /// 5. Расчет D-составляющей с фильтром: D = filter(-Kd * dPV/dt)
    /// 6. Расчет предварительного выхода: u_raw = P + I + D
    /// 7. Обработка режимов (ручной/автомат) с безударным переходом
    /// 8. Интегрирование методом трапеций с защитой от насыщения
    /// 9. Сохранение состояния для следующего шага
    pub fn tick(&mut self) {
        // Инициализация prev_pv при первом запуске во избежание скачка дифференциатора
        // При старте dPV/dt = 0, так как prev_pv = pv (нет изменения)
        if self.first_run {
            self.prev_pv = self.pv;
            self.first_run = false;
        }

        // 1. Определение ошибки рассогласования
        // e_k = SP_k - PV_k
        let mut er = self.sp - self.pv;

        // 2. Линейно-сдвинутая зона нечувствительности (без скачков на границах)
        // В отличие от простого обнуления, обеспечивает непрерывность сигнала
        // Математически:
        //   e_eff = e - sign(e) * db_boundary при |e| > db
        //   e_eff = 0 при |e| ≤ db
        // Это предотвращает скачок выходного сигнала при выходе ошибки из deadband
        if er > self.db_max {
            er -= self.db_max;  // e_eff > 0: сдвиг вправо от db_max
        } else if er < self.db_min {
            er -= self.db_min;  // e_eff < 0: сдвиг влево от db_min
        } else {
            er = 0.0;           // внутри deadband: выход не меняется
        }

        // 3. Пропорциональная составляющая
        // P_k = Kp * e_effective — мгновенная реакция на ошибку
        let p_part = er * self.kp;

        // 4. Дифференциальная составляющая с безусловно устойчивым ФНЧ 1-го порядка (Backward Euler)
        // Используем производную от PV (не от ошибки) для избежания derivative kick
        // при ступенчатом изменении уставки:
        //
        // raw_derivative = (PV_k - PV_{k-1}) / Ts
        // filtered = alpha * D_{k-1} + (1 - alpha) * (-Kd * raw_derivative)
        //
        // где alpha = 1 / (1 + Kdf * Ts) — коэффициент фильтра
        // Знак минус: рост PV уменьшает ошибку, d(e)/dt = -d(PV)/dt при постоянном SP
        //
        // Backward Euler гарантирует устойчивость фильтра при любых Kdf и Ts
        let mut d_part = 0.0;
        if self.kd > 0.0 && self.ts > 0.0 {
            // Дифференцирование по PV (избегаем реакции на изменение уставки)
            let derivative = (self.pv - self.prev_pv) / self.ts;
            
            // Расчет коэффициента фильтрации (Backward Euler метод)
            // alpha = 1 / (1 + Kdf * Ts) — всегда в диапазоне (0, 1]
            // Фильтр устойчив при любых Kdf > 0 и Ts > 0
            let alpha = 1.0 / (1.0 + self.kdf * self.ts);
            
            // Рекурсивная фильтрация: D_k = α * D_{k-1} + (1-α) * u_k
            // u_k = -Kd * d(PV)/dt — вход фильтра
            // Эквивалентная передаточная функция: D(s) = Kd * Td*s / (1 + s/Kdf)
            self.d_state = alpha * self.d_state + (1.0 - alpha) * (-derivative * self.kd);
            d_part = self.d_state;
        } else {
            self.d_state = 0.0; // Сброс состояния фильтра при Kd = 0
        }

        // 5. Вычисление предварительного сигнала в авто-режиме
        // u_raw = P_k + I_{k-1} + D_k (без ограничений)
        let auto_output = p_part + self.i_part + d_part;

        // 6. Логика безударного перехода и режимы работы
        if self.man_on {
            // === РУЧНОЙ РЕЖИМ ===
            // Выход = ручное значение, ограниченное физическими пределами
            self.op = self.manual.clamp(self.op_min, self.op_max);

            // Обратный расчет (Back-calculation) интегратора для безударного включения автомата
            // При переходе в автомат: u_manual = P + I + D  =>  I = u_manual - P - D
            // Это гарантирует что в первый момент после переключения выход не изменится
            if self.ki > 0.0 {
                self.i_part = self.op - p_part - d_part;
                self.i_part = self.i_part.clamp(self.op_min, self.op_max);
            } else {
                self.i_part = 0.0;
            }
        } else {
            // === АВТОМАТИЧЕСКИЙ РЕЖИМ ===
            // Применяем амплитудный ограничитель к выходу
            self.op = auto_output.clamp(self.op_min, self.op_max);

            // Синхронизация ручного канала управления для обратного безударного перехода
            // При следующем включении ручного режима, manual уже будет равен текущему выходу
            self.manual = self.op;

            // 7. Интегральная составляющая (Метод трапеций Tustin + защита от насыщения Clamping)
            // 
            // Метод трапеций (2-го порядка точности):
            // I_k = I_{k-1} + Ki * Ts * (e_k + e_{k-1}) / 2
            //
            // Защита от интегрального насыщения (Conditional Integration):
            // Интегратор активен ТОЛЬКО если:
            // - Регулятор не в насыщении, ИЛИ
            // - Регулятор в насыщении, но знак ошибки направлен на выход из насыщения
            //   (например, при верхнем насыщении и отрицательной ошибке)
            if self.ki > 0.0 && self.ts > 0.0 {
                let mut allow_integration = true;

                // Проверка условий насыщения
                if auto_output > self.op_max && er > 0.0 {
                    // Верхнее насыщение + положительная ошибка (толкает дальше вверх)
                    // → останавливаем интегратор для предотвращения windup
                    allow_integration = false;
                } else if auto_output < self.op_min && er < 0.0 {
                    // Нижнее насыщение + отрицательная ошибка (толкает дальше вниз)
                    // → останавливаем интегратор для предотвращения windup
                    allow_integration = false;
                }

                if allow_integration {
                    // Численное интегрирование методом трапеций:
                    // I_k = I_{k-1} + Ki * Ts * (e_k + e_{k-1}) / 2
                    // Трапеции дают O(Ts²) точность против O(Ts) для метода прямоугольников
                    self.i_part += 0.5 * (er + self.prev_er) * self.ki * self.ts;
                    
                    // Безопасное ограничение интегратора физическими пределами выхода
                    // Предотвращает неограниченный рост/падение даже при корректной работе anti-windup
                    self.i_part = self.i_part.clamp(self.op_min, self.op_max);
                }
            } else {
                self.i_part = 0.0; // Принудительный сброс при Ki = 0 или Ts = 0
            }
        }

        // Сохранение значений для использования на следующем шаге расчета
        self.prev_er = er;      // e_{k-1} для метода трапеций
        self.prev_pv = self.pv; // PV_{k-1} для расчета производной
    }
}


